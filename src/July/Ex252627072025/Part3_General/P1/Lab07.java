package July.Ex252627072025.Part3_General.P1;

public class Lab07 {
    public static void main(String[] args) {
        //pyramid
        for (int i = 1; i <= 6; i++) {
            for (int j = 1; j < i; j++) {
                System.out.print("*");
            }
            System.out.println(" ");
        }
    }
}
